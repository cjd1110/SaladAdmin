package com.kosta.admin.dto.delivery;

public class StockDto {
	private int s_pnumber;
	private String s_pname;
	private int s_price;
	private int s_quantity;
	private int s_gram;
	private String s_image;
	private String s_category;
	private String s_content;
	
	public int getS_pnumber() {
		return s_pnumber;
	}
	public void setS_pnumber(int s_pnumber) {
		this.s_pnumber = s_pnumber;
	}
	public String getS_pname() {
		return s_pname;
	}
	public void setS_pname(String s_pname) {
		this.s_pname = s_pname;
	}
	public int getS_price() {
		return s_price;
	}
	public void setS_price(int s_price) {
		this.s_price = s_price;
	}
	public int getS_quantity() {
		return s_quantity;
	}
	public void setS_quantity(int s_quantity) {
		this.s_quantity = s_quantity;
	}
	public int getS_gram() {
		return s_gram;
	}
	public void setS_gram(int s_gram) {
		this.s_gram = s_gram;
	}
	public String getS_image() {
		return s_image;
	}
	public void setS_image(String s_image) {
		this.s_image = s_image;
	}
	public String getS_category() {
		return s_category;
	}
	public void setS_category(String s_category) {
		this.s_category = s_category;
	}
	public String getS_content() {
		return s_content;
	}
	public void setS_content(String s_content) {
		this.s_content = s_content;
	}
	
	
}
